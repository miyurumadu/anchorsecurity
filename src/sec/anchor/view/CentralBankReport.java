    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        dtm = (DefaultTableModel) centralBankTable.getModel();

        try {

            ArrayList<EmpSalary> getCentralBankReport = EmpSalaryController.loadCentralBankReport(monthCombo.getSelectedItem() + "", yearCombo.getSelectedItem() + "");
            dtm.setRowCount(0);
            // SalaryCons searchBasicSal = SalaryConstController.searchBasicSal();
            // double basic = searchBasicSal.getBasic_salary() + searchBasicSal.getBudget_al();
            int number = 0;
            for (EmpSalary empSalary : getCentralBankReport) {

                String[] rows = {(++number) + "", empSalary.getName() + "", empSalary.getNic() + "", empSalary.getEpf() + "", (empSalary.getEpfEmp() + empSalary.getEpfcompany()) + "", empSalary.getEpfcompany() + "", empSalary.getEpfEmp() + "", SalaryConstController.searchBasicSal(empSalary.getNic()) + ""};
                contribution += SalaryConstController.searchBasicSal(empSalary.getNic());
                dtm.addRow(rows);

            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ViewAllEmployee.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

