  private void centralBankReportPrint() {
        try {

            JasperReport jasperReport = JasperCompileManager.compileReport("./src/sec/anchor/reports/EPFReport.jrxml");
            Map<String, Object> param = new HashMap<>();
            int year = Year.now().getValue();
            param.put("MONTH", monthCombo.getSelectedItem() + "-" + year);
            param.put("CON", contribution + "");
            param.put("SUR", 0.00 + "");
            param.put("REM", contribution + "");
            param.put("BANK", bankCombo.getSelectedItem() + "");
            param.put("BRANCH", branchName.getText());
            param.put("CHECK", checknoText.getText());

            JasperPrint jp;
            dtm = (DefaultTableModel) centralBankTable.getModel();
            JRTableModelDataSource tabledatesource = new JRTableModelDataSource(dtm);
            jp = JasperFillManager.fillReport(jasperReport, param, tabledatesource);
            JasperViewer.viewReport(jp, false);

        } catch (JRException ex) {
            Logger.getLogger(PaySalary.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

